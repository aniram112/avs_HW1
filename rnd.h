#ifndef __rnd__
#define __rnd__

#include <cstdlib>

//------------------------------------------------------------------------------
// rnd.h - содержит генератор случайных чисел в диапазоне от 97 до 122 (для символов) или от 1 до 10 (для чисел)
//------------------------------------------------------------------------------

inline auto Random() {
    return rand() % 26 + 97;
}

inline auto RandomSmall() {
    return rand() % 10+1;
}
#endif //__rnd__
